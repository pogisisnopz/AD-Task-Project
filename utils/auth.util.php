    $requestUri = $_SERVER['REQUEST_URI'];
    
    error_log("ğŸ” [$scriptName] isAuthenticated() called - Session ID: $sessionId - URI: $requestUri");
    error_log("ğŸ” [$scriptName] Current session data: " . json_encode($_SESSION));
    
    // Check both authentication methods
    $hasUser = isset($_SESSION['user']);
    $hasSimpleAuth = isset($_SESSION['simple_auth']);
    
    error_log("ğŸ” [$scriptName] Has user session: " . ($hasUser ? 'YES' : 'NO'));
    error_log("ğŸ” [$scriptName] Has simple_auth: " . ($hasSimpleAuth ? 'YES' : 'NO'));
    
