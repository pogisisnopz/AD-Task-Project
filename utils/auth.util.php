function requireLogin(): void {
    $scriptName = basename($_SERVER['SCRIPT_NAME']);
    error_log("ğŸ”’ [$scriptName] requireLogin() called");
    
    if (!isAuthenticated()) {
        error_log("ğŸ”’ [$scriptName] User not authenticated, redirecting to login");
