function authenticate(string $username, string $password): bool {
    global $pdo;
    $scriptName = basename($_SERVER['SCRIPT_NAME']);
    
    error_log("ğŸ” [$scriptName] authenticate() called for user: $username");
    error_log("ğŸ” [$scriptName] Session before auth: " . json_encode($_SESSION));

    try {
        // Query the database for the user
        $stmt = $pdo->prepare("SELECT * FROM users WHERE username = :username");
        $stmt->bindParam(':username', $username, PDO::PARAM_STR);
        $stmt->execute();
        
        $user = $stmt->fetch(PDO::FETCH_ASSOC);
        
        if ($user && $user['password'] === $password) {
            // Store user data in session
            $_SESSION['user'] = [
                'id' => $user['id'] ?? 1,
                'username' => $user['username'],
                'first_name' => $user['first_name'],
                'last_name' => $user['last_name'],
                'email' => $user['email'] ?? $user['username'] . '@mechanicus.com',
                'role' => mapRoleToTitle($user['role'])
            ];
            
            error_log("âœ… [$scriptName] AUTH SUCCESS for $username from database");
            error_log("âœ… [$scriptName] Session after DB auth: " . json_encode($_SESSION));
            return true;
        }
        
        error_log("âŒ [$scriptName] AUTH FAILED for $username - user not found or wrong password");
        return false;
        
    } catch (PDOException $e) {
        error_log("âŒ [$scriptName] Database error during authentication: " . $e->getMessage());
        
        // Fallback to hardcoded credentials if database fails
        error_log("ğŸ”„ [$scriptName] Falling back to hardcoded credentials");
        return authenticateFallback($username, $password);
    }
}

